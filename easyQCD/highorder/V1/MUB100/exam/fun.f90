SUBROUTINE IRENHA(K,ASHIFT,DASHIFT)
  
  IMPLICIT NONE

  REAL(16),INTENT(IN) :: K
  REAL(16),INTENT(OUT) :: ASHIFT,DASHIFT
!  REAL(16),PARAMETER :: IRSTRENGTH=0.034Q+0
  REAL(16),PARAMETER :: IRSTRENGTH=0.018Q+0
  REAL(16),PARAMETER :: CUT=2.Q+3
  INTEGER(2),PARAMETER :: N=2

  IF((K/CUT)**N>150.Q+0)THEN
    ASHIFT=1.Q+0
