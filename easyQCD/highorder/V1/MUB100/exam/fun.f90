    ASHIFT=1.Q+0 + (IRSTRENGTH*(K/CUT)**N)/(-1.Q+0 + EXP((K/CUT)**N))
    DASHIFT=-((IRSTRENGTH*(K/CUT)**N*(1.Q+0 + EXP((K/CUT)**N)*(-1.Q+0 + (K/CUT)**N))*N)/ &
    (-1.Q+0 + EXP((K/CUT)**N))**2)
  END IF
END SUBROUTINE IRENHA

SUBROUTINE MASSASCREEN(K,T,DMASS2S,PTDMASS2S)
  
  USE CONST_SAVE , ONLY  : PI
  IMPLICIT NONE

  REAL(16),INTENT(IN) :: K,T
  REAL(16),INTENT(OUT) :: DMASS2S,PTDMASS2S
  REAL(16) :: KT
  REAL(16),PARAMETER :: CS=2.Q+0
  INTEGER(2),PARAMETER :: NS=2

  KT=PI*T

  DMASS2S=CS**2*T**2*EXP(-(K/KT)**NS)
  PTDMASS2S=-((CS**2*K*(K/KT)**(-1 + NS)*NS*T**2*EXP(-(K/KT)**NS))/KT)
END SUBROUTINE MASSASCREEN
