SUBROUTINE POLYAKOVMOD_CAL(T)

  USE POLYAKOV_PER 
  IMPLICIT NONE
  REAL(16),INTENT(IN) :: T

  REAL(16),PARAMETER   :: A1=-44.14Q+0
  REAL(16),PARAMETER   :: A2=151.4Q+0
  REAL(16),PARAMETER   :: A3=-90.0677Q+0
  REAL(16),PARAMETER   :: A4=2.77173Q+0
  REAL(16),PARAMETER   :: A5=3.56403Q+0

  REAL(16),PARAMETER   :: B1=-0.32665Q+0
  REAL(16),PARAMETER   :: B2=-82.9823Q+0
  REAL(16),PARAMETER   :: B3=3.Q+0
  REAL(16),PARAMETER   :: B4=5.85559Q+0

  REAL(16),PARAMETER   :: C1=-50.7961Q+0
  REAL(16),PARAMETER   :: C2=114.038Q+0
  REAL(16),PARAMETER   :: C3=-89.4596Q+0
  REAL(16),PARAMETER   :: C4=3.08718Q+0
  REAL(16),PARAMETER   :: C5=6.72812Q+0

  REAL(16),PARAMETER   :: D1=27.0885Q+0
  REAL(16),PARAMETER   :: D2=-56.0859Q+0
  REAL(16),PARAMETER   :: D3=71.2225Q+0
  REAL(16),PARAMETER   :: D4=2.9715Q+0
  REAL(16),PARAMETER   :: D5=6.61433Q+0

  REAL(16),PARAMETER   :: T0=225.Q+0
  REAL(16) :: T_R

  T_R=0.57Q+0*(T-T0)/T0+1.Q+0

  APOLYA=(A1+A2/T_R+A3/T_R**2)/(1.Q+0+A4/T_R+A5/T_R**2)
  CPOLYA=(C1+C2/T_R+C3/T_R**2)/(1.Q+0+C4/T_R+C5/T_R**2)
  DPOLYA=(D1+D2/T_R+D3/T_R**2)/(1.Q+0+D4/T_R+D5/T_R**2)

  BPOLYA=B1*T_R**(-B4)*(1.Q+0-EXP(B2/T_R**B3))
END SUBROUTINE POLYAKOVMOD_CAL
